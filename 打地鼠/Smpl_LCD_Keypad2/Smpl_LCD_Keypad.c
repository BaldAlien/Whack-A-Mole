int32_t main(void)
{
	int8_t number;

	// char text[15]="Key =   ";
	// char mole_text[15] = "mole = ";
	// char whack_text[15] = "whack = ";
	// char res_text[15] = "res = ";

	int8_t mole_state = 1;		   //地鼠状态，1代表活的，0代表死的
	int32_t mole_rand_count = 100; //地鼠刷新计时器
	int8_t mole_now = 1;   //获得一只地鼠

	UNLOCKREG();
	SYSCLK->PWRCON.XTL32K_EN = 1;
	SYSCLK->PWRCON.XTL12M_EN = 1; //Enable 12Mhz and set HCLK->12Mhz
	SYSCLK->CLKSEL0.HCLK_S = 0;
	LOCKREG();

	Initial_panel();
	clr_all_panel();

	OpenKeyPad();

	InitData();
	while (1)
	{
		if (mole_rand_count) //判断地鼠是否要刷新
		{
			mole_rand_count--; //不用刷新，计数-1
		}
		else
		{
			LoadImg(0, mole_now);	//刷走上一只地鼠
			mole_now = GetRand();  //刷新地鼠
			// InitMoleHole();
			LoadImg(1, mole_now);
			mole_state = 1;		   //活的地鼠
			mole_rand_count = 100; //地鼠刷新时间重置
		}
		// mole_now = GetRand();
		number = Scankey(); //读取键盘

		if (mole_now == (number - 3)) //如果刚好打中当前地鼠
		{
			LoadImg(2, mole_now);		//地鼠变为死的
			mole_state = 0;
		}

		close_seven_segment();
		show_seven_segment(0, number); //数码管显示键盘输入

		// sprintf(mole_text + 7, "%d", mole_now);
		// sprintf(whack_text + 8, "%d", number);
		// if (mole_state)
		// {
		// 	sprintf(res_text + 6, "live");
		// }
		// else
		// {
		// 	sprintf(res_text + 6, "死 ");
		// }
		LoadBuf();
		// print_lcd(0, mole_text);
		// print_lcd(1, whack_text);
		// print_lcd(2, res_text);
		draw_LCD(DisplayBuf); // render to LCD
		Delay(5000);
	}
}
